#include <stdio.h>
/**
 * add - adds two integers.
 * @a: the first integer
 * @b: the second integer
 * Return: sum of a and b
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - adds two integers.
 * @a: the first integer
 * @b: the second integer
 * Return: sub of a and b
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplies two integers.
 * @a: the first integer
 * @b: the second integer
 * Return: multplication of a and b
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divides two integers.
 * @a: the first integer
 * @b: the second integer
 * Return:division of a and b
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod -check modula of two integers.
 * @a: the first integer
 * @b: the second integer
 * Return: % of a and b
 */
int mod(int a, int b)
{
	return (a % b);
}
